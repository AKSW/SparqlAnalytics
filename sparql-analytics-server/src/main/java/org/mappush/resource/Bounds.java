package org.mappush.resource;

public class Bounds {
	private int x;
	private int y;
	
	public Bounds() {
		super();
	}
	public Bounds(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
}